#Estudo de variaveis e tipos de dados
#O intuito é se aprofundar na lógica de programação da linguagem python.

idade: int
salario: float
altura: float
genero: str
nome: str

idade = 20
salario = 5800.5
altura = 1.73
genero = "F"
nome = "Maria Silva"

print(f"IDADE = {idade}")
print(f"SALARIO = {salario:.2f}")
print(f"ALTURA = {altura:.2f}")
print(f"GENERO = {genero}")
print(f"NOME = {nome}")

#O python não necessariamente precisa que os tipos sejam declarados, o python é capaz de inferir o tipo de cada uma.